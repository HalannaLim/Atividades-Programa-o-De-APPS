#4 - Faça um programa que leia 5 números e informe o maior número.

n1= float(input("Insira o primeiro número:"))
n2= float(input("Insira o segundo número:"))
n3= float(input("Insira o terceiro número:"))
n4= float(input("Insira o quarto número:"))
n5= float(input("Insira o quinto número:"))

maiornum = max(n1,n2,n3,n4,n5)

print("O maior número é:", maiornum)
